bool kpk::probe(const square wk_sq, const square wp_sq, const square bk_sq, const side me)
{
	assert(file_of(wp_sq) <= file_d);
	const auto idx = index(me, bk_sq, wk_sq, wp_sq);
	return kpk_bitbase[idx / 32] & 1 << (idx & 0x1F);
}
